int		ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	cou;

	i = 0;
	cou = 0;
	while (tab[i] != 0)
	{
		if (f(tab[i]) == 1)
			cou++;
		i++;
	}
	return (cou);
}
